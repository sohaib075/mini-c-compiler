/* Test 3: If-Else Control Flow */
/* Tests: if, if-else, relational operators, nested if */
int main() {
    int x = 10;
    int y = 20;
    int max;

    if (x > y) {
        max = x;
    } else {
        max = y;
    }

    int result = 0;
    if (max == 20) {
        result = 1;
    }

    return 0;
}
